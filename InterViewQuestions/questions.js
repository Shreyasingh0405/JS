// 2- Explain the difference between JavaScript and Java.
//Ans:- - **Purpose**:  
// - **JavaScript**: Primarily used for creating interactive web content.
// - **Java**: Used for building a wide range of applications, including enterprise, Android, and desktop apps.

// - **Execution**:  
// - **JavaScript**: Runs in web browsers or on servers using Node.js.
// - **Java**: Compiled into bytecode and runs on the Java Virtual Machine (JVM).

// - **Syntax**:  
// - **JavaScript**: Loosely typed and supports multiple paradigms like functional and event-driven programming.
// - **Java**: Strongly typed, primarily object-oriented, and focuses on classes and objects.

// - **Concurrency**:  
// - **JavaScript**: Single-threaded, with asynchronous capabilities using promises and async/await.
// - **Java**: Supports multi-threading for parallel processing.

// - **Use Cases**:  
// - **JavaScript**: Web development, game development, and mobile apps.
// - **Java**: Enterprise systems, Android apps, and large-scale applications.

